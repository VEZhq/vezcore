function normalizeIconToken(value: string): string {
  return value.replace(/[^a-z0-9]/gi, '').toLowerCase()
}

export function normalizeServiceIconName(iconName: string | null | undefined): string | null {
  if (!iconName) return null
  const trimmed = iconName.trim()
  if (!trimmed) return null

  const normalized = normalizeIconToken(trimmed)
  const match = SERVICE_ICON_OPTIONS.find((option) => normalizeIconToken(option) === normalized)
  return match ?? trimmed
}

export function getServiceIcon(iconName: string | null | undefined): LucideIcon | null {
  const normalizedName = normalizeServiceIconName(iconName)
  if (!normalizedName) return null
  if (!isValidServiceIcon(normalizedName)) return null
  return SERVICE_ICON_MAP[normalizedName]
}

export function getServiceIconTextFallback(iconName: string | null | undefined): string {
  if (!iconName) return '—'
  const trimmed = iconName.trim()
  return trimmed.length > 0 ? trimmed : '—'
}

export function isValidServiceIcon(iconName: string | null | undefined): iconName is ServiceIconName {
  if (!iconName) return false
  return SERVICE_ICON_OPTIONS.includes(iconName as ServiceIconName)
}
